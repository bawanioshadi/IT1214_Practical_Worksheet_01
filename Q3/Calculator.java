class Calculator {
    // Add two integers
    int add(int a, int b) {
        return a + b;
    }

    // Overloaded method: add three integers
    int add(int a, int b, int c) {
        return a + b + c;
    }

    // Overloaded method: add two doubles
    double add(double a, double b) {
        return a + b;
    }
}

